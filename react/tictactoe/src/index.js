import React from 'react';
import ReactDOM from 'react-dom';
import './index.css';
import LCC from 'lightning-container';

function Square(props) {
    return (
        <button className="square" onClick={props.onClick}>
            {props.value}
        </button>
    );
}

class Board extends React.Component {
    renderSquare(i) {
        return (
            <Square
                value={this.props.squares[i]}
                onClick={() => this.props.onClick(i)}
            />
        );
    }

    render() {
        return (
            <div>
                <div className="board-row">
                    {this.renderSquare(0)}
                    {this.renderSquare(1)}
                    {this.renderSquare(2)}
                </div>
                <div className="board-row">
                    {this.renderSquare(3)}
                    {this.renderSquare(4)}
                    {this.renderSquare(5)}
                </div>
                <div className="board-row">
                    {this.renderSquare(6)}
                    {this.renderSquare(7)}
                    {this.renderSquare(8)}
                </div>
            </div>
        );
    }
}

class Game extends React.Component {
    constructor(props) {
        super(props);
        this.state = {
            history: [
                {
                    squares: Array(9).fill(null)
                }
            ],
            stepNumber: 0,
            xIsNext: true,
            points: 0,
            apexCalled: false
        };

        this.handleSetWinnerResponse = this.handleSetWinnerResponse.bind(this);
    }

    handleClick(i) {
        const history = this.state.history.slice(0, this.state.stepNumber + 1);
        const current = history[history.length - 1];
        const squares = current.squares.slice();
        if (calculateWinner(squares) || squares[i]) {
            return;
        }
        squares[i] = this.state.xIsNext ? "X" : "O";
        this.setState({
            history: history.concat([
                {
                    squares: squares
                }
            ]),
            stepNumber: history.length,
            xIsNext: !this.state.xIsNext
        });

        console.log('Sending message');
        LCC.sendMessage({name:'move', value: squares});
    }

    jumpTo(step) {
        if (step === 0)
            this.state.apexCalled = false;
        this.setState({
            stepNumber: step,
            xIsNext: (step % 2) === 0
        });
    }

    render() {
        const history = this.state.history;
        const points = this.state.points;
        const apexCalled = this.state.apexCalled;
        const current = history[this.state.stepNumber];
        const winner = calculateWinner(current.squares);

        const moves = history.map((step, move) => {
            const desc = move ?
                'Go to move #' + move :
                'Go to game start';
            return (
                <li key={move}>
                    <button onClick={() => this.jumpTo(move)}>{desc}</button>
                </li>
            );
        });

        let status;
        if (winner) {
            if (!apexCalled) {
                LCC.callApex("TestReactContainerController.setWinner",
                                winner,
                                this.handleSetWinnerResponse,
                                {escape: true});
            }
            status = "Winner: " + winner + " - Points:" + points;
        } else {
            status = "Next player: " + (this.state.xIsNext ? "X" : "O");
        }

        return (
            <div className="game">
                <div className="game-board">
                    <Board
                        squares={current.squares}
                        onClick={i => this.handleClick(i)}
                    />
                </div>
                <div className="game-info">
                    <div id="status-panel">{status}</div>
                    <ol>{moves}</ol>
                </div>
            </div>
        );
    }

    handleSetWinnerResponse(result, event) {
        if (event.status) {
            console.log('winner correctly passed to apex controller');
            this.setState({
                points: result,
                apexCalled: true
            });
        } else if (event.type === "exception") {
            console.log(event.message + " : " + event.where);
        }
    }
}

// ========================================

const wrapper = document.getElementById("container");
var element = wrapper ? ReactDOM.render(<Game />, wrapper) : false;

LCC.addMessageHandler(function(message) {
    element.jumpTo(0);
});

function calculateWinner(squares) {
    const lines = [
        [0, 1, 2],
        [3, 4, 5],
        [6, 7, 8],
        [0, 3, 6],
        [1, 4, 7],
        [2, 5, 8],
        [0, 4, 8],
        [2, 4, 6]
    ];
    for (let i = 0; i < lines.length; i++) {
        const [a, b, c] = lines[i];
        if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
            return squares[a];
        }
    }
    return null;
}

